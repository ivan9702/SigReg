function printr( $object ) {
  if ( is_array( $object ) ) {
    print( '<pre>' );
    print_r( $object );
    print( '</pre>' );
  } elseif ( is_string($object) || is_int($object) ) {

    print( '<h3>' );
    print_r( $object );
    print( '</h3>' );
  } else {
    var_dump( $object );
  }
  flush();
}

